// --- カテゴリ選択 (Step 1) ---
function selectCategory(catKey) {
    currentKeys.cat = catKey; 
    currentKeys.sub = '';
    
    // サイドメニュー等のボタンのActive状態を制御
    document.querySelectorAll('#main-categories button').forEach(b => {
        const onClickAttr = b.getAttribute('onclick') || "";
        b.classList.toggle('active', onClickAttr.includes(`'${catKey}'`));
    });

    renderSubButtons(catKey);
    document.getElementById('sub-sub-categories').innerHTML = ''; // 下位をクリア
    updateBreadcrumb(catKey);
}

// --- サブカテゴリボタン作成 (Step 2) ---
function renderSubButtons(catKey) {
    const container = document.getElementById('sub-categories');
    container.innerHTML = '';
    for (let key in data[catKey].sub) {
        const btn = document.createElement('button');
        btn.innerText = data[catKey].sub[key].name;
        btn.onclick = (e) => {
            container.querySelectorAll('button').forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');
            currentKeys.sub = key;
            updateBreadcrumb(catKey, key);
            renderItems(catKey, key);
        };
        container.appendChild(btn);
    }
}

// --- 製品リスト表示 (Step 3) ---
function renderItems(catKey, subKey) {
    const container = document.getElementById('sub-sub-categories');
    container.innerHTML = '';
    const items = data[catKey].sub[subKey].sub;

    for (let key in items) {
        const item = items[key];
        const row = document.createElement('div');
        row.className = 'item-row';

        // 選択肢の重複を排除して取得
        const cols = [...new Set(item.variants.map(v => v.color))];
        const sizs = [...new Set(item.variants.map(v => v.size))];
        const firstPrice = item.variants[0].price || item.price;

        row.innerHTML = `
            <span style="flex:1"><strong>${item.name}</strong></span>
            
            <div style="display: flex; flex-direction: column; margin-right: 10px;">
                <label style="font-size: 0.65rem; color: #888; margin-bottom: 2px;">カラー</label>
                <select id="col-${key}" onchange="updatePriceDisplay('${key}','${catKey}','${subKey}')">
                    ${cols.map(c => `<option>${c}</option>`).join('')}
                </select>
            </div>

            <div style="display: flex; flex-direction: column; margin-right: 10px;">
                <label style="font-size: 0.65rem; color: #888; margin-bottom: 2px;">サイズ/長さ</label>
                <select id="siz-${key}" onchange="updatePriceDisplay('${key}','${catKey}','${subKey}')">
                    ${sizs.map(s => `<option>${s}</option>`).join('')}
                </select>
            </div>

            <span id="price-display-${key}" style="min-width:100px; text-align:right; margin-right:15px; font-weight:bold; align-self: flex-end; padding-bottom: 5px;">
                ¥${firstPrice.toLocaleString()}
            </span>
            
            <button class="add-btn" onclick="addItem('${key}','${catKey}','${subKey}')" style="align-self: flex-end; margin-bottom: 2px;">追加</button>
        `;
        container.appendChild(row);
    }
}

// --- 価格表示のリアルタイム更新 ---
function updatePriceDisplay(k, ck, sk) {
    const item = data[ck].sub[sk].sub[k];
    const c = document.getElementById(`col-${k}`).value;
    const s = document.getElementById(`siz-${k}`).value;
    
    const v = item.variants.find(v => v.color === c && v.size === s);
    if (v) {
        const price = v.price || item.price;
        document.getElementById(`price-display-${k}`).innerText = `¥${price.toLocaleString()}`;
    }
}

// --- 見積もりへの追加 ---
function addItem(k, ck, sk) {
    const item = data[ck].sub[sk].sub[k];
    const c = document.getElementById(`col-${k}`).value;
    const s = document.getElementById(`siz-${k}`).value;
    
    const v = item.variants.find(v => v.color === c && v.size === s) || item.variants[0];
    const price = v.price || item.price;
    const variantStr = `${c} / ${s}`;
    const uniqueID = `${item.name}-${variantStr}`;

    const ex = currentEstimateItems.find(i => i.uniqueID === uniqueID);

    if (ex) {
        ex.quantity++; 
    } else {
        currentEstimateItems.push({ 
            uniqueID, 
            name: item.name, 
            variant: variantStr, 
            price: price, 
            quantity: 1, 
            catName: data[ck].name, 
            subName: data[ck].sub[sk].name 
        });
    }

    showToast(`${item.name} (${variantStr}) を追加しました`);
    renderTable();
}

function renderTable() {
    const tbody = document.getElementById('summary-list');
    if (!tbody) return;
    tbody.innerHTML = '';

    // 1. ソート処理
    currentEstimateItems.sort((a, b) => 
        a.catName.localeCompare(b.catName, 'ja') || 
        a.subName.localeCompare(b.subName, 'ja') || 
        a.name.localeCompare(b.name, 'ja')
    );

    let total = 0;
    let totalQty = 0;

    // 2. HTML作成と計算
currentEstimateItems.forEach((item, i) => {
    const subtotal = item.price * item.quantity;
    total += subtotal;
    totalQty += item.quantity;

    // 製品名の長さによってクラスを分岐（20文字以上なら小さく）
    const nameClass = item.name.length > 20 ? 'font-small' : '';
    // 金額が7桁（1,000,000〜）ならクラスを付与
    const priceClass = subtotal >= 1000000 ? 'font-narrow' : '';

    tbody.innerHTML += `
    <tr>
        <td class="col-name ${nameClass}">
            <strong>${item.name}</strong><br>
            <small style="color:#888;">${item.variant}</small>
        </td>
        <td class="col-price">¥${item.price.toLocaleString()}</td>
        <td class="col-qty">
            <div class="qty-btn-container">
                <button class="qty-btn minus" onclick="updateQty(${i},-1)">-</button>
                <span class="qty-display">${item.quantity}</span>
                <button class="qty-btn plus" onclick="updateQty(${i},1)">+</button>
            </div>
        </td>
        <td class="col-subtotal ${priceClass}">¥${subtotal.toLocaleString()}</td>
        <td class="col-delete">
            <button class="delete-btn" onclick="del(${i})">✕</button>
        </td>
    </tr>`;
});

// --- 3. 合計金額・数量の表示更新とサイズ調整 ---
    const totalPriceElem = document.getElementById('total-price');
    const totalQtyElem = document.getElementById('total-qty');

    if (totalPriceElem) {
        // カンマ区切りの金額を代入
        totalPriceElem.innerText = total.toLocaleString();

        // 7桁以上（1,000,000〜）ならフォントを小さく、文字間を詰める
        if (total >= 1000000) {
            totalPriceElem.style.fontSize = "2.2rem"; 
            totalPriceElem.style.letterSpacing = "-2px";
        } else {
            totalPriceElem.style.fontSize = "2.8rem";
            totalPriceElem.style.letterSpacing = "normal";
        }
    }

    if (totalQtyElem) {
        totalQtyElem.innerText = totalQty;
    }

    // --- 4. ローカルストレージに保存 ---
    localStorage.setItem('petzl_final_v5', JSON.stringify(currentEstimateItems));
}

// --- 操作系関数 ---
function updateQty(i, d) { 
    currentEstimateItems[i].quantity += d; 
    if (currentEstimateItems[i].quantity < 1) currentEstimateItems[i].quantity = 1; 
    renderTable(); 
}

function del(i) { 
    currentEstimateItems.splice(i, 1); 
    renderTable(); 
}

function resetEstimate() { 
    if(confirm("見積もりをすべてリセットしますか？")){ 
        currentEstimateItems=[]; 
        renderTable(); 
        showToast("リセットしました");
    } 
}

// --- パンくずリスト更新 (リンク機能付き) ---
function updateBreadcrumb(catKey = null, subKey = null) {
    const bread = document.getElementById('breadcrumb');
    let html = '<span class="breadcrumb-link" onclick="resetToStep1()">TOP</span>';

    if (catKey) {
        const catName = data[catKey].name;
        html += ` ＞ <span class="breadcrumb-link" onclick="selectCategory('${catKey}')">${catName}</span>`;
    }

    if (subKey) {
        const subName = data[catKey].sub[subKey].name;
        html += ` ＞ <span class="active-step">${subName}</span>`;
    }

    bread.innerHTML = html;
}

// Step 1（最初）に戻る
function resetToStep1() {
    document.getElementById('sub-categories').innerHTML = '';
    document.getElementById('sub-sub-categories').innerHTML = '';
    updateBreadcrumb();
    document.querySelectorAll('#main-categories button').forEach(b => b.classList.remove('active'));
}

// --- トースト通知機能 ---
function showToast(message) {
    let container = document.getElementById('toast-container');
    if (!container) {
        container = document.createElement('div');
        container.id = 'toast-container';
        document.body.appendChild(container);
    }

    const toast = document.createElement('div');
    toast.className = 'toast';
    toast.innerText = message;
    container.appendChild(toast);

    setTimeout(() => {
        toast.classList.add('fade-out');
        setTimeout(() => toast.remove(), 400);
    }, 2500);
}
// 入力欄の内容を印刷用の表示エリアに同期させる
document.addEventListener('DOMContentLoaded', () => {
    // 入力要素
    const inComp = document.getElementById('input-company');
    const inUser = document.getElementById('input-user');
    const inTel = document.getElementById('input-tel');

    // 表示要素
    const dispComp = document.getElementById('display-company');
    const dispUser = document.getElementById('display-user');
    const dispTel = document.getElementById('display-tel');

    // 同期関数
    const sync = () => {
        dispComp.innerText = inComp.value || "（会社名）";
        dispUser.innerText = inUser.value || "（担当者名）";
        dispTel.innerText = inTel.value ? "TEL: " + inTel.value : "TEL: （電話番号）";
    };

    if(inComp) inComp.addEventListener('input', sync);
    if(inUser) inUser.addEventListener('input', sync);
    if(inTel) inTel.addEventListener('input', sync);
});
