let currentEstimateItems = [];
let currentKeys = { cat: '', sub: '' };

window.onload = () => {
    const saved = localStorage.getItem('petzl_final_v5');
    if (saved) {
        currentEstimateItems = JSON.parse(saved);
        renderTable();
    }
};

function selectCategory(catKey) {
    currentKeys.cat = catKey; currentKeys.sub = '';
    renderSubButtons(catKey);
    document.getElementById('sub-sub-categories').innerHTML = '';
    updateBreadcrumb();
}

function renderSubButtons(catKey) {
    const container = document.getElementById('sub-categories');
    container.innerHTML = '';
    for (let key in data[catKey].sub) {
        const btn = document.createElement('button');
        btn.innerText = data[catKey].sub[key].name;
        btn.onclick = (e) => {
            container.querySelectorAll('button').forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');
            currentKeys.sub = key;
            updateBreadcrumb();
            renderItems(catKey, key);
        };
        container.appendChild(btn);
    }
}

function renderItems(catKey, subKey) {
    const container = document.getElementById('sub-sub-categories');
    container.innerHTML = '';
    const items = data[catKey].sub[subKey].sub;

    for (let key in items) {
        const item = items[key];
        const row = document.createElement('div');
        row.className = 'item-row';

        const cols = [...new Set(item.variants.map(v => v.color))];
        const sizs = [...new Set(item.variants.map(v => v.size))];

        const firstPrice = item.variants[0].price || item.price;

        row.innerHTML = `
            <span style="flex:1"><strong>${item.name}</strong></span>
            <select id="col-${key}" onchange="updatePriceDisplay('${key}','${catKey}','${subKey}')">
                ${cols.map(c => `<option>${c}</option>`).join('')}
            </select>
            <select id="siz-${key}">
                ${sizs.map(s => `<option>${s}</option>`).join('')}
            </select>
            <span id="price-display-${key}" style="min-width:100px; text-align:right; margin-right:15px; font-weight:bold;">
                ¥${firstPrice.toLocaleString()}
            </span>
            <button class="add-btn" onclick="addItem('${key}','${catKey}','${subKey}')">追加</button>
        `;
        container.appendChild(row);
    }
}

// カラー選択時に価格表示を更新
function updatePriceDisplay(k, ck, sk) {
    const item = data[ck].sub[sk].sub[k];
    const c = document.getElementById(`col-${k}`).value;
    const v = item.variants.find(v => v.color === c);
    const price = v ? (v.price || item.price) : item.price;
    document.getElementById(`price-display-${k}`).innerText = `¥${price.toLocaleString()}`;
}

function addItem(k, ck, sk) {
    const item = data[ck].sub[sk].sub[k];
    const c = document.getElementById(`col-${k}`).value;
    const s = document.getElementById(`siz-${k}`).value;
    
    const v = item.variants.find(v => v.color === c && v.size === s) || item.variants.find(v => v.color === c);
    const price = v.price || item.price;
    const variantStr = `${c} / ${s}`;

    const uniqueID = `${item.name}-${variantStr}`;
    const ex = currentEstimateItems.find(i => i.uniqueID === uniqueID);

    if (ex) {
        ex.quantity++; 
    } else {
        currentEstimateItems.push({ 
            uniqueID, name: item.name, variant: variantStr, price: price, quantity: 1, 
            catName: data[ck].name, subName: data[ck].sub[sk].name 
        });
    }
    renderTable();
}

function renderTable() {
    const tbody = document.getElementById('summary-list');
    tbody.innerHTML = '';
    currentEstimateItems.sort((a, b) => a.catName.localeCompare(b.catName, 'ja') || a.subName.localeCompare(b.subName, 'ja') || a.name.localeCompare(b.name, 'ja'));

    let total = 0;
    currentEstimateItems.forEach((item, i) => {
        total += item.price * item.quantity;
        tbody.innerHTML += `<tr>
            <td><strong>${item.name}</strong><br><small>${item.variant}</small></td>
            <td style="text-align:right;">¥${item.price.toLocaleString()}</td>
            <td><div class="qty-btn-container"><button onclick="updateQty(${i},-1)">-</button> ${item.quantity} <button onclick="updateQty(${i},1)">+</button></div></td>
            <td style="text-align:right; font-weight:bold;">¥${(item.price * item.quantity).toLocaleString()}</td>
            <td style="text-align:center;"><button class="delete-btn" onclick="del(${i})">✕</button></td>
        </tr>`;
    });
    document.getElementById('total-price').innerText = total.toLocaleString();
    localStorage.setItem('petzl_final_v5', JSON.stringify(currentEstimateItems));
}

function updateQty(i, d) { currentEstimateItems[i].quantity += d; if (currentEstimateItems[i].quantity < 1) currentEstimateItems[i].quantity = 1; renderTable(); }
function del(i) { currentEstimateItems.splice(i, 1); renderTable(); }
function resetEstimate() { if(confirm("リセットしますか？")){ currentEstimateItems=[]; renderTable(); } }
function exportCSV() {
    let csv = "\uFEFF製品名,仕様,単価,数量,小計\n";
    currentEstimateItems.forEach(i => csv += `"${i.name}","${i.variant}",${i.price},${i.quantity},${i.price*i.quantity}\n`);
    const blob = new Blob([csv], { type: 'text/csv' });
    const a = document.createElement("a"); a.href = URL.createObjectURL(blob); a.download = "estimate.csv"; a.click();
}
function updateBreadcrumb() {
    const bc = document.getElementById('breadcrumb');
    bc.innerHTML = '<span class="breadcrumb-link" onclick="location.reload()">TOP</span>';
    if (currentKeys.cat) bc.innerHTML += ` > ${data[currentKeys.cat].name}`;
    if (currentKeys.sub) bc.innerHTML += ` > ${data[currentKeys.cat].sub[currentKeys.sub].name}`;
}